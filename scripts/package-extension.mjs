#!/usr/bin/env node
// Builds the Chrome Web Store-ready archive from extension/ only.

import { execFileSync } from 'node:child_process';
import { copyFileSync, existsSync, mkdirSync, readdirSync, readFileSync, rmSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const EXT_ROOT = join(ROOT, 'extension');
const DIST_DIR = join(ROOT, 'dist');

function fail(message) {
  console.error(`package-extension: ${message}`);
  process.exit(1);
}

const manifestPath = join(EXT_ROOT, 'manifest.json');
if (!existsSync(manifestPath)) {
  fail('extension/manifest.json not found');
}

let manifest;
try {
  manifest = JSON.parse(readFileSync(manifestPath, 'utf8'));
} catch (error) {
  fail(`extension/manifest.json is not valid JSON: ${error.message}`);
}

if (!/^(\d+\.){2,3}\d+$/.test(manifest.version || '')) {
  fail(`extension/manifest.json version "${manifest.version}" is not a Chrome extension version`);
}

mkdirSync(DIST_DIR, { recursive: true });

const zipName = `fsb-extension-v${manifest.version}.zip`;
const zipPath = join(DIST_DIR, zipName);
rmSync(zipPath, { force: true });

// ---- D-16: ship the catalog (recipes + descriptors) into the package -------
//
// catalog/ is a top-level dir and is NOT under extension/, so the zip-of-EXT_ROOT
// step below would never include it -- a packaged build would have an EMPTY index
// (Pitfall 4). Generate a build-time dual-export IIFE at extension/catalog/
// recipe-index.generated.js that inlines { recipes, descriptors } as a JSON
// literal and sets global.FsbRecipeIndex. Because it lives UNDER extension/, the
// existing zip ships it automatically (no new zip arg). It is MV3-cold-start-safe
// (no service-worker fetch race). The _fixtures/ subdirs are test-only and are
// EXCLUDED. The generated source is pure data and free of dynamic-code constructs.
function readJsonDir(absDir) {
  if (!existsSync(absDir)) return [];
  const files = [];
  function walk(dir, relPrefix = '') {
    for (const name of readdirSync(dir).sort()) {
      if (name === '_fixtures') continue;
      const abs = join(dir, name);
      const rel = relPrefix ? join(relPrefix, name) : name;
      const st = statSync(abs);
      if (st.isDirectory()) {
        walk(abs, rel);
      } else if (name.endsWith('.json')) {
        files.push({ abs, rel });
      }
    }
  }
  walk(absDir);
  return files
    .sort((a, b) => a.rel.localeCompare(b.rel))
    .map((file) => {
      try {
        return JSON.parse(readFileSync(file.abs, 'utf8'));
      } catch (error) {
        fail(`catalog file ${file.rel} is not valid JSON: ${error.message}`);
        return null;
      }
    });
}

const CATALOG_ROOT = join(ROOT, 'catalog');
const recipes = readJsonDir(join(CATALOG_ROOT, 'recipes'));
const descriptors = readJsonDir(join(CATALOG_ROOT, 'descriptors'));

const generatedDir = join(EXT_ROOT, 'catalog');
mkdirSync(generatedDir, { recursive: true });
const generatedPath = join(generatedDir, 'recipe-index.generated.js');
const catalogData = JSON.stringify({ recipes, descriptors }, null, 2);
const generatedSource =
  '// GENERATED by scripts/package-extension.mjs -- DO NOT EDIT BY HAND.\n' +
  '// Build-time catalog snapshot (D-16): recipes + descriptors shipped into the\n' +
  '// extension package so the capability-search index has data in a packaged build.\n' +
  '// Pure data dual-export IIFE; loaded via importScripts before capability-search.js.\n' +
  '(function(global) {\n' +
  "  'use strict';\n" +
  '  var DATA = ' + catalogData + ';\n' +
  '  global.FsbRecipeIndex = DATA;\n' +
  "  if (typeof module !== 'undefined' && module.exports) { module.exports = DATA; }\n" +
  '})(typeof globalThis !== \'undefined\' ? globalThis : this);\n';
writeFileSync(generatedPath, generatedSource, 'utf8');
console.log(
  `package-extension: generated ${generatedPath.replace(`${ROOT}/`, '')} ` +
  `(${recipes.length} recipes, ${descriptors.length} descriptors)`
);

// ---- Phase 29 (CAT-02, D-10): ship the bundled head handlers into the package -
//
// Handlers (catalog/handlers/*.js) are reviewed CODE, not data -- unlike recipes
// they CANNOT be JSON-inlined, so they are COPIED verbatim under extension/catalog/
// handlers/ where the existing zip-of-EXT_ROOT picks them up (the same 28-D-16 trap
// the recipe-index step above fixes for recipes). The dev tree also tracks these
// extension-root handler copies so unpacked loads work before packaging; this step
// overwrites them from the canonical catalog/handlers source before zipping. No
// per-handler importScripts manifest is generated here -- those load lines are added
// in Plan 02/03.
const HANDLERS_SRC = join(CATALOG_ROOT, 'handlers');
const HANDLERS_DEST = join(EXT_ROOT, 'catalog', 'handlers');
let copiedHandlerCount = 0;
if (existsSync(HANDLERS_SRC)) {
  mkdirSync(HANDLERS_DEST, { recursive: true });
  const handlerFiles = readdirSync(HANDLERS_SRC).filter((name) => name.endsWith('.js'));
  for (const name of handlerFiles) {
    copyFileSync(join(HANDLERS_SRC, name), join(HANDLERS_DEST, name));
    copiedHandlerCount++;
  }
}
console.log(
  `package-extension: copied ${copiedHandlerCount} handler module(s) into ` +
  `extension/catalog/handlers/ (absent catalog/handlers/ tolerated)`
);

const excludes = [
  '.DS_Store',
  '*/.DS_Store',
  '*.log',
  '*.tmp',
  '*.temp',
  '*.pem',
  'config/secrets.json',
  'config/dev-*',
  'config/local-*',
  'node_modules/*',
  'build/*',
];

try {
  execFileSync('zip', ['-r', '-q', zipPath, '.', '-x', ...excludes], {
    cwd: EXT_ROOT,
    stdio: 'inherit',
  });
} catch (error) {
  if (error.code === 'ENOENT') {
    fail('zip CLI is required to build the extension archive');
  }
  process.exit(error.status || 1);
}

console.log(`package-extension: wrote ${zipPath.replace(`${ROOT}/`, '')}`);
