  /**
   * Infer the semantic purpose of an element
   * Classifies elements by their role, intent, and sensitivity
   * @param {Element} element - DOM element to analyze
   * @returns {Object} Purpose classification with role, intent, and flags
   */
  function inferElementPurpose(element) {
    const text = (element.textContent || '').toLowerCase().trim();
    const ariaLabel = (element.getAttribute('aria-label') || '').toLowerCase();
    const className = getClassName(element).toLowerCase();
    const type = element.type?.toLowerCase() || '';
    const name = (element.name || '').toLowerCase();
    const id = (element.id || '').toLowerCase();
    const placeholder = (element.placeholder || '').toLowerCase();
    const autocomplete = (element.getAttribute('autocomplete') || '').toLowerCase();
    const role = (element.getAttribute('role') || '').toLowerCase();

    const combined = `${text} ${ariaLabel} ${className} ${name} ${id} ${placeholder} ${autocomplete}`;

    // FSB-injected canvas editor elements (Google Docs/Sheets)
    if (element.dataset && element.dataset.fsbRole) {
      const fsbRole = element.dataset.fsbRole;
      if (fsbRole === 'document-body' || fsbRole === 'document-title') {
        return { role: 'content-editor', intent: 'edit', danger: false, sensitive: false, priority: 'high' };
      }
      if (fsbRole === 'formula-bar' || fsbRole === 'name-box') {
        return { role: 'toolbar-input', intent: 'edit', danger: false, sensitive: false, priority: 'high' };
      }
      return { role: 'editor-container', intent: 'edit', danger: false, sensitive: false, priority: 'medium' };
    }

    // Contenteditable elements (Google Docs, Notion, Medium, rich text editors)
    if (element.getAttribute('contenteditable') === 'true' || element.isContentEditable) {
      const rect = element.getBoundingClientRect();
      const isLargeEditor = rect.width > 200 && rect.height > 100;
      if (isLargeEditor || /editor|page|content|body|compose|write|kix|ql-editor|ProseMirror/i.test(className + id + role)) {
        return { role: 'content-editor', intent: 'edit', danger: false, sensitive: false, priority: 'high' };
      }
      return { role: 'inline-editor', intent: 'edit', danger: false, sensitive: false, priority: 'medium' };
    }

    // Button/action classification
    if (element.tagName === 'BUTTON' || element.tagName === 'A' || type === 'submit' || role === 'button') {
      // Primary submit actions
      if (/submit|send|post|confirm|done|finish|complete|checkout|pay|purchase|buy|order/.test(combined)) {
        return { role: 'primary-action', intent: 'submit', danger: false, priority: 'high' };
      }

      // Search actions
      if (/search|find|look|go|query/.test(combined)) {
        return { role: 'search-action', intent: 'search', danger: false, priority: 'high' };
      }

      // Login actions
      if (/login|sign.?in|log.?in|authenticate/.test(combined)) {
        return { role: 'auth-action', intent: 'login', danger: false, priority: 'high' };
      }

      // Signup/registration
      if (/signup|sign.?up|register|create.?account|join/.test(combined)) {
        return { role: 'auth-action', intent: 'signup', danger: false, priority: 'high' };
      }

      // Navigation forward
      if (/next|continue|proceed|forward|advance/.test(combined)) {
        return { role: 'navigation', intent: 'next-step', danger: false, priority: 'medium' };
      }

      // Navigation backward
      if (/back|previous|return|go.?back/.test(combined)) {
        return { role: 'navigation', intent: 'back', danger: false, priority: 'low' };
      }

      // Cancel/dismiss actions
      if (/cancel|close|dismiss|no|decline|skip|later|not.?now/.test(combined)) {
        return { role: 'secondary-action', intent: 'cancel', danger: false, priority: 'low' };
      }

      // Destructive actions
      if (/delete|remove|destroy|clear|reset|unsubscribe/.test(combined)) {
        return { role: 'destructive-action', intent: 'delete', danger: true, priority: 'low' };
      }

      // Edit actions
      if (/edit|modify|change|update/.test(combined)) {
        return { role: 'edit-action', intent: 'edit', danger: false, priority: 'medium' };
      }

      // Add/create actions
      if (/add|create|new|plus|\+/.test(combined)) {
        return { role: 'create-action', intent: 'add', danger: false, priority: 'medium' };
      }

      // Share/social actions
      if (/share|tweet|post|send|invite/.test(combined)) {
        return { role: 'social-action', intent: 'share', danger: false, priority: 'medium' };
      }

      // Download actions
      if (/download|export|save.?as/.test(combined)) {
        return { role: 'download-action', intent: 'download', danger: false, priority: 'medium' };
      }

      // If it has text, it's likely a meaningful action button
      if (text.length > 0 && text.length < 30) {
        return { role: 'action-button', intent: 'interact', danger: false, priority: 'medium' };
      }
    }

    // Input field classification
    if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA' || element.tagName === 'SELECT') {
      // Password fields
      if (type === 'password' || /password|pwd|pass/.test(name + id)) {
        return { role: 'credential-input', intent: 'password', sensitive: true, priority: 'high' };
      }

      // Email fields
      if (type === 'email' || /email|e-mail|mail/.test(combined)) {
        return { role: 'contact-input', intent: 'email', sensitive: false, priority: 'high' };
      }

      // Username/login fields
      if (/username|user.?name|user.?id|login|account/.test(combined)) {
        return { role: 'credential-input', intent: 'username', sensitive: false, priority: 'high' };
      }

      // Search input fields
      if (type === 'search' || /search|query|q$|find|keyword/.test(name + id + className) ||
          role === 'searchbox' || element.getAttribute('aria-label')?.toLowerCase().includes('search')) {
        return { role: 'search-input', intent: 'search-query', sensitive: false, priority: 'high' };
      }

      // Payment/card fields
      if (autocomplete.includes('cc-number') || /card number|credit card number|debit card number|cc.?number|card no\b/.test(combined)) {
        return { role: 'payment-input', intent: 'cc-number', sensitive: true, priority: 'high' };
      }
      if (autocomplete.includes('cc-csc') || autocomplete.includes('cc-cvc') || /cvv|cvc|csc|security code|card code|verification code/.test(combined)) {
        return { role: 'payment-input', intent: 'cc-csc', sensitive: true, priority: 'high' };
      }
      if (autocomplete.includes('cc-exp-month') || /(exp|expiration).*(month)|month.*(exp|expiration)/.test(combined)) {
        return { role: 'payment-input', intent: 'cc-exp-month', sensitive: false, priority: 'high' };
      }
      if (autocomplete.includes('cc-exp-year') || /(exp|expiration).*(year)|year.*(exp|expiration)/.test(combined)) {
        return { role: 'payment-input', intent: 'cc-exp-year', sensitive: false, priority: 'high' };
      }
      if (autocomplete.includes('cc-exp') || /expiration date|expiry date|exp date|mm\s*\/\s*yy|mm\s*\/\s*yyyy/.test(combined)) {
        return { role: 'payment-input', intent: 'cc-exp', sensitive: false, priority: 'high' };
      }
      if (autocomplete.includes('cc-name') || /name on card|cardholder|card holder|name as it appears/.test(combined)) {
        return { role: 'payment-input', intent: 'cc-name', sensitive: false, priority: 'high' };
      }
      if ((autocomplete.includes('billing') || /billing/.test(combined)) && (autocomplete.includes('postal-code') || /zip|postal/.test(combined))) {
        return { role: 'payment-input', intent: 'billing-postal-code', sensitive: false, priority: 'high' };
      }
      if ((autocomplete.includes('billing') || /billing/.test(combined)) && (autocomplete.includes('address-line2') || /address line 2|apt|suite|unit/.test(combined))) {
        return { role: 'payment-input', intent: 'billing-address-line2', sensitive: false, priority: 'medium' };
      }
      if ((autocomplete.includes('billing') || /billing/.test(combined)) && (autocomplete.includes('address-line1') || autocomplete.includes('street-address') || /address|street/.test(combined))) {
        return { role: 'payment-input', intent: 'billing-address-line1', sensitive: false, priority: 'high' };
      }
      if ((autocomplete.includes('billing') || /billing/.test(combined)) && (autocomplete.includes('address-level2') || /city/.test(combined))) {
        return { role: 'payment-input', intent: 'billing-city', sensitive: false, priority: 'medium' };
      }
      if ((autocomplete.includes('billing') || /billing/.test(combined)) && (autocomplete.includes('address-level1') || /state|province|region/.test(combined))) {
        return { role: 'payment-input', intent: 'billing-region', sensitive: false, priority: 'medium' };
      }
      if ((autocomplete.includes('billing') || /billing/.test(combined)) && (autocomplete.includes('country') || /country/.test(combined))) {
        return { role: 'payment-input', intent: 'billing-country', sensitive: false, priority: 'medium' };
      }
      if ((autocomplete.includes('billing') || /billing/.test(combined)) && /name/.test(combined)) {
        return { role: 'payment-input', intent: 'billing-name', sensitive: false, priority: 'medium' };
      }
      if (/card|credit|debit|payment|billing/.test(combined)) {
        return { role: 'payment-input', intent: 'payment', sensitive: true, priority: 'high' };
      }

      // Phone fields
      if (type === 'tel' || /phone|tel|mobile|cell/.test(combined)) {
        return { role: 'contact-input', intent: 'phone', sensitive: false, priority: 'medium' };
      }

      // Name fields
      if (/first.?name|last.?name|full.?name|^name$/.test(name + id)) {
        return { role: 'personal-input', intent: 'name', sensitive: false, priority: 'medium' };
      }

      // Address fields
      if (/address|street|city|state|zip|postal|country/.test(combined)) {
        return { role: 'address-input', intent: 'address', sensitive: false, priority: 'medium' };
      }

      // Date fields
      if (type === 'date' || /date|birth|dob/.test(combined)) {
        return { role: 'date-input', intent: 'date', sensitive: false, priority: 'medium' };
      }

      // Quantity/number fields
      if (type === 'number' || /quantity|qty|amount|count/.test(combined)) {
        return { role: 'numeric-input', intent: 'quantity', sensitive: false, priority: 'medium' };
      }

      // URL fields
      if (type === 'url' || /url|website|link|href/.test(combined)) {
        return { role: 'url-input', intent: 'url', sensitive: false, priority: 'low' };
      }

      // General text input
      if (type === 'text' || element.tagName === 'TEXTAREA') {
        // Check for message/comment context
        if (/message|comment|note|description|content|body|text/.test(combined)) {
          return { role: 'text-input', intent: 'message', sensitive: false, priority: 'medium' };
        }
        return { role: 'text-input', intent: 'general-text', sensitive: false, priority: 'low' };
      }

      // Checkbox/radio
      if (type === 'checkbox' || type === 'radio') {
        if (/agree|terms|consent|accept|remember|subscribe/.test(combined)) {
          return { role: 'consent-input', intent: 'agreement', sensitive: false, priority: 'medium' };
        }
        return { role: 'toggle-input', intent: 'selection', sensitive: false, priority: 'low' };
      }

      // Select dropdowns
      if (element.tagName === 'SELECT') {
        return { role: 'select-input', intent: 'selection', sensitive: false, priority: 'medium' };
      }
    }

    // Link classification
    if (element.tagName === 'A') {
      const href = (element.href || '').toLowerCase();

      if (/logout|sign.?out|log.?out/.test(combined + href)) {
        return { role: 'auth-link', intent: 'logout', danger: true, priority: 'low' };
      }

      if (/help|support|faq|contact/.test(combined + href)) {
        return { role: 'help-link', intent: 'help', danger: false, priority: 'low' };
      }

      if (/privacy|terms|policy|legal/.test(combined + href)) {
        return { role: 'legal-link', intent: 'legal', danger: false, priority: 'low' };
      }

      return { role: 'navigation-link', intent: 'navigate', danger: false, priority: 'low' };
    }

    return { role: 'unknown', intent: null, danger: false, sensitive: false, priority: 'low' };
  }

  // Get relationship context for an element (modal, form, navigation, region)
  // Returns a descriptive string like "in checkout form" or "in main navigation"
  function getRelationshipContext(element) {
    const contexts = [];

    // Check if element is in a modal/dialog
    const modal = element.closest('[role="dialog"], [role="alertdialog"], dialog, .modal, [aria-modal="true"]');
    if (modal) {
      const modalTitle = modal.querySelector('[role="heading"], h1, h2, h3, .modal-title')?.textContent?.trim();
      if (modalTitle && modalTitle.length < 50) {
        contexts.push(`in "${modalTitle}" modal`);
      } else {
        contexts.push('in modal');
      }
    }

    // Check if element is in a form (skip if already in modal context)
    if (!modal) {
      const form = element.closest('form');
      if (form) {
        // Try to get form identifier
        const formLabel = form.getAttribute('aria-label');
        const formName = form.name;
        const formId = form.id;

        // Try to infer form purpose from heading or action
        const formHeading = form.querySelector('h1, h2, h3, legend')?.textContent?.trim();
        const formAction = form.action;

        let formIdentifier = null;

        if (formLabel && formLabel.length < 50) {
          formIdentifier = formLabel;
        } else if (formHeading && formHeading.length < 50) {
          formIdentifier = formHeading;
        } else if (formId && formId !== '' && !formId.match(/^form[-_]?\d*$/i)) {
          // Use ID if it's meaningful (not just "form" or "form_1")
          formIdentifier = formId.replace(/[-_]/g, ' ');
        } else if (formName && formName !== '' && !formName.match(/^form[-_]?\d*$/i)) {
          formIdentifier = formName.replace(/[-_]/g, ' ');
        } else if (formAction) {
          // Try to infer from action URL
          const actionPath = formAction.split('/').pop()?.split('?')[0]?.replace(/[-_]/g, ' ');
          if (actionPath && actionPath.length > 2 && actionPath.length < 30) {
            formIdentifier = actionPath;
          }
        }

        if (formIdentifier) {
          contexts.push(`in ${formIdentifier} form`);
        } else {
          contexts.push('in form');
        }
      }
    }

    // Check if element is in navigation
    const nav = element.closest('nav, [role="navigation"], header nav, .nav, .navigation, .navbar, .menu');
    if (nav) {
      const navLabel = nav.getAttribute('aria-label');
      if (navLabel && navLabel.length < 30) {
        contexts.push(`in ${navLabel}`);
      } else if (nav.closest('header')) {
        contexts.push('in main navigation');
      } else if (nav.closest('footer')) {
        contexts.push('in footer navigation');
      } else {
        contexts.push('in navigation');
      }
    }

    // Check for semantic regions (if not already in nav or form)
    if (contexts.length === 0) {
      const region = element.closest('header, footer, aside, main, article, section[aria-label], [role="banner"], [role="main"], [role="complementary"], [role="contentinfo"]');
      if (region) {
        const regionTag = region.tagName.toLowerCase();
        const regionLabel = region.getAttribute('aria-label');
        const regionRole = region.getAttribute('role');

        if (regionLabel && regionLabel.length < 30) {
          contexts.push(`in ${regionLabel}`);
        } else if (regionTag === 'header' || regionRole === 'banner') {
          contexts.push('in header');
        } else if (regionTag === 'footer' || regionRole === 'contentinfo') {
          contexts.push('in footer');
        } else if (regionTag === 'aside' || regionRole === 'complementary') {
          contexts.push('in sidebar');
        } else if (regionTag === 'main' || regionRole === 'main') {
          contexts.push('in main content');
        } else if (regionTag === 'article') {
          const articleTitle = region.querySelector('h1, h2, h3')?.textContent?.trim();
          if (articleTitle && articleTitle.length < 40) {
            contexts.push(`in "${articleTitle}" article`);
          } else {
            contexts.push('in article');
          }
        }
      }
    }

    // Check for list context (useful for repeated items)
    const listItem = element.closest('li');
    if (listItem) {
      const list = listItem.closest('ul, ol');
      if (list) {
        const listLabel = list.getAttribute('aria-label');
        if (listLabel && listLabel.length < 30) {
          contexts.push(`in ${listLabel} list`);
        }
      }
    }

    // Check for card/tile context (common UI pattern)
    const card = element.closest('.card, [class*="card"], .tile, [class*="tile"], article:not(article article)');
    if (card && !element.closest('nav')) {
      const cardTitle = card.querySelector('h1, h2, h3, h4, .card-title, [class*="title"]')?.textContent?.trim();
      if (cardTitle && cardTitle.length < 40 && cardTitle.length > 2) {
        contexts.push(`in "${cardTitle}" card`);
      }
    }

    // Return the most specific context (first one found), or empty string
    return contexts.length > 0 ? contexts[0] : '';
  }

  // Generate human-readable description of element (enhanced with purpose)
  function generateElementDescription(element) {
    const parts = [];

    // Get element purpose for enhanced description
    const purpose = inferElementPurpose(element);

    // Start with element type description
    const typeDescriptions = {
      'button': 'button',
      'a': 'link',
      'input': element.type ? `${element.type} input` : 'input field',
      'select': 'dropdown',
      'textarea': 'text area',
      'img': 'image',
      'video': 'video',
      'audio': 'audio',
      'iframe': 'embedded frame'
    };

    parts.push(typeDescriptions[element.tagName.toLowerCase()] || element.tagName.toLowerCase());

    // Add purpose-based description if available
    if (purpose.role !== 'unknown') {
      parts.push(`[${purpose.role}:${purpose.intent}]`);
    }

    // Add descriptive text
    const ariaLabel = element.getAttribute('aria-label');
    const text = element.textContent?.trim();
    const placeholder = element.placeholder;
    const alt = element.alt;
    const title = element.title;

    if (ariaLabel) {
      parts.push(`"${ariaLabel}"`);
    } else if (text && text.length > 0 && text.length < 50) {
      parts.push(`"${text}"`);
    } else if (placeholder) {
      parts.push(`with placeholder "${placeholder}"`);
    } else if (alt) {
      parts.push(`"${alt}"`);
    } else if (title) {
      parts.push(`"${title}"`);
    }

    // Add visual characteristics
    const styles = window.getComputedStyle(element);
    const bgColor = styles.backgroundColor;
    const color = styles.color;

    // Add color if it's distinctive
    if (bgColor && bgColor !== 'rgba(0, 0, 0, 0)' && bgColor !== 'transparent') {
      const colorName = getColorName(bgColor);
      if (colorName) parts.push(colorName);
    }

    // Add position context
    const rect = element.getBoundingClientRect();
    const viewport = { width: window.innerWidth, height: window.innerHeight };

    if (rect.top < viewport.height * 0.2) {
      parts.push('at top');
    } else if (rect.bottom > viewport.height * 0.8) {
      parts.push('at bottom');
    }

    if (rect.left < viewport.width * 0.3) {
      parts.push('left side');
    } else if (rect.right > viewport.width * 0.7) {
      parts.push('right side');
    } else if (rect.left > viewport.width * 0.4 && rect.right < viewport.width * 0.6) {
      parts.push('center');
    }

    // Add relationship context (form, navigation, modal, region)
    const relationshipContext = getRelationshipContext(element);
    if (relationshipContext) {
      parts.push(relationshipContext);
    }

    // Add danger/sensitive flags
    if (purpose.danger) {
      parts.push('[DESTRUCTIVE]');
    }
    if (purpose.sensitive) {
      parts.push('[SENSITIVE]');
    }

    return parts.join(' ');
  }

  // Helper function to get color name from RGB
  function getColorName(rgbString) {
    // Simple color detection - could be enhanced
    const rgb = rgbString.match(/\d+/g);
    if (!rgb || rgb.length < 3) return null;

    const [r, g, b] = rgb.map(Number);

    // Basic color detection
    if (r > 200 && g < 100 && b < 100) return 'red';
    if (r < 100 && g > 200 && b < 100) return 'green';
    if (r < 100 && g < 100 && b > 200) return 'blue';
    if (r > 200 && g > 200 && b < 100) return 'yellow';
    if (r > 200 && g < 150 && b > 200) return 'purple';
    if (r > 200 && g > 100 && b < 50) return 'orange';
    if (r > 200 && g > 200 && b > 200) return 'white';
    if (r < 50 && g < 50 && b < 50) return 'black';
    if (r > 100 && g > 100 && b > 100 && r < 200) return 'gray';

    return null;
  }

  // Get element clustering information
  function getElementCluster(element) {
    const cluster = {
      formContext: null,
      sectionContext: null,
      nearbyButtons: [],
      nearbyLabels: [],
      siblingInputs: [],
      navigationContext: null
    };

    // Find parent form
    const form = element.closest('form');
    if (form) {
      cluster.formContext = {
        id: form.id || null,
        name: form.name || null,
        action: form.action || null,
        ariaLabel: form.getAttribute('aria-label') || null,
        totalFields: form.querySelectorAll('input, select, textarea').length
      };
    }

    // Find parent section/article
    const section = element.closest('section, article, aside, nav, header, footer, main, [role]');
    if (section) {
      cluster.sectionContext = {
        tag: section.tagName.toLowerCase(),
        id: section.id || null,
        role: section.getAttribute('role') || null,
        ariaLabel: section.getAttribute('aria-label') || null,
        heading: section.querySelector('h1, h2, h3, h4, h5, h6')?.textContent?.trim() || null
      };
    }

    // Find nearby interactive elements
    const parent = element.parentElement;
    if (parent) {
      // Find sibling buttons
      const siblingButtons = Array.from(parent.querySelectorAll('button, [role="button"], input[type="submit"], input[type="button"]'))
        .filter(btn => btn !== element)
        .slice(0, 3)
        .map(btn => ({
          text: btn.textContent?.trim() || btn.value || '',
          type: btn.tagName.toLowerCase(),
          ariaLabel: btn.getAttribute('aria-label') || null
        }));
      cluster.nearbyButtons = siblingButtons;

      // Find sibling inputs (for form context)
      if (element.tagName === 'INPUT' || element.tagName === 'SELECT' || element.tagName === 'TEXTAREA') {
        const siblingInputs = Array.from(parent.querySelectorAll('input, select, textarea'))
          .filter(input => input !== element)
          .slice(0, 3)
          .map(input => ({
            type: input.type || input.tagName.toLowerCase(),
            name: input.name || null,
            placeholder: input.placeholder || null,
            label: input.labels?.[0]?.textContent?.trim() || null
          }));
        cluster.siblingInputs = siblingInputs;
      }
    }

    // Find nearby labels
    const nearbyLabels = [];

    // Check for associated label
    if (element.labels && element.labels.length > 0) {
      nearbyLabels.push(...Array.from(element.labels).map(label => label.textContent?.trim()));
    }

    // Check for aria-labelledby
    const labelledBy = element.getAttribute('aria-labelledby');
    if (labelledBy) {
      const labelElement = document.getElementById(labelledBy);
      if (labelElement) {
        nearbyLabels.push(labelElement.textContent?.trim());
      }
    }

    // Check for preceding label-like elements
    const prevSibling = element.previousElementSibling;
    if (prevSibling && ['LABEL', 'SPAN', 'DIV'].includes(prevSibling.tagName)) {
      const text = prevSibling.textContent?.trim();
      if (text && text.length < 50) {
        nearbyLabels.push(text);
      }
    }

    cluster.nearbyLabels = [...new Set(nearbyLabels)]; // Remove duplicates

    // Find navigation context
    const nav = element.closest('nav, [role="navigation"]');
    if (nav) {
      cluster.navigationContext = {
        ariaLabel: nav.getAttribute('aria-label') || null,
        totalLinks: nav.querySelectorAll('a').length,
        currentPage: nav.querySelector('[aria-current="page"]')?.textContent?.trim() || null
      };
    }

    return cluster;
  }

