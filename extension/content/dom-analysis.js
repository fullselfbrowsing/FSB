  /**
   * Extracts and structures DOM information for AI processing
   */
  function getStructuredDOM(options = {}) {
    const {
      useDiffing = false,
      prioritizeViewport = true,
      viewportOnlyMode = true,
      viewportComplete = false,
      maxElements = 50,
      includeAllAttributes = false,
      includeComputedStyles = false
    } = options;

    // Access cross-module singletons
    const domStateManager = FSB.domStateManager;
    const generateSelectors = FSB.generateSelectors;

    // PERFORMANCE: Viewport dimensions for prioritization
    const viewportRect = {
      top: 0,
      left: 0,
      bottom: window.innerHeight,
      right: window.innerWidth
    };

    // PERFORMANCE: Separate collections for viewport vs offscreen elements
    const viewportElements = [];
    const offscreenElements = [];
    let totalElementCount = 0;

    // Also extract raw HTML for better context
    const relevantHTML = extractRelevantHTML();

    // Helper to check if element is in viewport
    function isInViewportRect(rect) {
      return rect.bottom >= viewportRect.top &&
             rect.top <= viewportRect.bottom &&
             rect.right >= viewportRect.left &&
             rect.left <= viewportRect.right;
    }

    // Infer task type from page context for relevance scoring
    function inferTaskTypeFromContext() {
      const url = window.location.href.toLowerCase();
      const hasSearchInput = document.querySelector('[type="search"], [role="searchbox"]');
      const hasLoginForm = document.querySelector('input[type="password"]');
      const hasForms = document.forms.length > 0;

      if (/search|results|q=/.test(url) || hasSearchInput) return 'search';
      if (/login|signin/.test(url) || hasLoginForm) return 'login';
      if (/checkout|payment|cart/.test(url)) return 'checkout';
      if (hasForms) return 'form';
      return 'general';
    }

    // Use 3-stage filtered elements
    const filteredElements = getFilteredElements({
      maxElements,
      prioritizeViewport,
      taskType: inferTaskTypeFromContext()
    });

    // Capture previous element hashes for new-element detection
    const hasPreviousState = domStateManager && domStateManager.elementCache.size > 0;
    const previousElementHashes = hasPreviousState ? new Set(domStateManager.elementCache.keys()) : new Set();

    // Process filtered elements
    filteredElements.forEach(node => {
      try {
        if (node.nodeType !== Node.ELEMENT_NODE) return;

        const rect = node.getBoundingClientRect();

        const semanticId = generateSemanticElementId(node, totalElementCount);

        // Stamp data-fsb-id so tools can locate this element by its snapshot ID
        try { node.setAttribute('data-fsb-id', semanticId); } catch (_e) {}

        const elementData = {
          elementId: semanticId,
          type: node.tagName.toLowerCase(),
          description: generateElementDescription(node),
          purpose: inferElementPurpose(node),
          visualProperties: getVisualProperties(node),
          inShadowDOM: isInShadowDOM(node),
          shadowPath: isInShadowDOM(node) ? getShadowPath(node) : null,
          text: node.innerText?.trim() || node.textContent?.trim() || '',
          id: node.id || '',
          class: node.className ? String(node.className) : '',
          position: {
            x: Math.round(rect.left),
            y: Math.round(rect.top),
            width: Math.round(rect.width),
            height: Math.round(rect.height),
            inViewport: isElementInViewport(rect)
          },
          attributes: {},
          dataAttributes: {},
          visibility: {
            display: window.getComputedStyle(node).display,
            visibility: window.getComputedStyle(node).visibility,
            opacity: window.getComputedStyle(node).opacity,
            zIndex: window.getComputedStyle(node).zIndex
          },
          interactionState: {
            disabled: node.disabled || false,
            readonly: node.readOnly || false,
            checked: node.checked || false,
            selected: node.selected || false,
            focused: document.activeElement === node
          },
          accessibilityName: computeAccessibleName(node),
          implicitRole: getImplicitRole(node),
          ariaRelationships: getARIARelationships(node),
          actionability: isElementActionable(node),
          selectors: generateSelectors(node, semanticId).map(s => s.selector),
          cluster: getElementCluster(node),
          context: {
            labelText: (() => {
              if (node.tagName === 'INPUT' || node.tagName === 'TEXTAREA' || node.tagName === 'SELECT') {
                const labelElement = node.id ? document.querySelector(`label[for="${node.id}"]`) :
                                   node.closest('label') ||
                                   node.parentElement?.querySelector('label');
                return labelElement ? labelElement.textContent?.trim() : null;
              }
              return null;
            })(),
            formId: node.form ? (node.form.id || node.form.name || 'form_' + Array.from(document.forms).indexOf(node.form)) : null,
            parentContext: node.parentElement ? {
              tag: node.parentElement.tagName,
              class: node.parentElement.className,
              role: node.parentElement.getAttribute('role'),
              'aria-label': node.parentElement.getAttribute('aria-label')
            } : null,
            hintText: node.placeholder || node.title || node.getAttribute('aria-describedby') ?
                     document.getElementById(node.getAttribute('aria-describedby'))?.textContent?.trim() : null
          },
          relationshipContext: getRelationshipContext(node)
        };

        // Flag new elements
        if (hasPreviousState) {
          const hash = domStateManager.hashElement(elementData);
          if (!previousElementHashes.has(hash)) {
            elementData.isNew = true;
          }
        }

        // Special handling for different element types
        if (node.tagName === 'INPUT') {
          elementData.inputType = node.type || '';
          elementData.placeholder = node.placeholder || '';
          elementData.value = node.value || '';
          elementData.isInput = true;
        } else if (node.tagName === 'BUTTON') {
          elementData.isButton = true;
        } else if (node.tagName === 'A') {
          elementData.href = node.href || '';
          elementData.isLink = true;
        } else if (node.tagName === 'IMG') {
          elementData.src = node.src || '';
          elementData.alt = node.alt || '';
          elementData.isImage = true;
        }

        // CAPTCHA detection
        if (node.hasAttribute('data-sitekey')) {
          const style = window.getComputedStyle(node);
          const captchaRect = node.getBoundingClientRect();
          if (style.display !== 'none' && style.visibility !== 'hidden' &&
              style.opacity !== '0' && captchaRect.width >= 30 && captchaRect.height >= 30) {
            elementData.isCaptcha = true;
          }
        }

        // Add attributes
        if (includeAllAttributes) {
          Array.from(node.attributes).forEach(attr => {
            elementData.attributes[attr.name] = attr.value;
            if (attr.name.startsWith('data-')) {
              elementData.dataAttributes[attr.name] = attr.value;
            }
          });
        } else {
          ['data-testid', 'aria-label', 'name', 'role', 'type', 'value', 'placeholder', 'title', 'alt', 'autocomplete'].forEach(attr => {
            if (node.getAttribute(attr)) {
              elementData.attributes[attr] = node.getAttribute(attr);
            }
          });
        }

        // Form relationships
        if (node.form) {
          elementData.formId = node.form.id || `form_${Array.from(document.forms).indexOf(node.form)}`;
        }

        // Label associations
        if (node.labels && node.labels.length > 0) {
          elementData.labelText = Array.from(node.labels).map(l => l.textContent?.trim()).join(' ');
        }

        // Shadow DOM
        if (node.shadowRoot && node.shadowRoot.mode === 'open') {
          logger.logDOMOperation(FSB.sessionId, 'shadow_dom_found', { tagName: node.tagName, id: node.id || node.className });
          elementData.hasShadowRoot = true;
          elementData.shadowRootMode = 'open';
        }

        // Skip screen-reader-only elements
        if (rect.left < -500 || rect.top < -500 || rect.right < -500 || rect.bottom < -500) {
          return;
        }

        // Sort into viewport vs offscreen
        const inViewport = isInViewportRect(rect);
        if (inViewport) {
          viewportElements.push(elementData);
        } else if (!viewportOnlyMode) {
          offscreenElements.push(elementData);
        }
        totalElementCount++;

      } catch (error) {
        logger.error('Error processing DOM node - CRITICAL', {
          sessionId: FSB.sessionId,
          error: error.message,
          stack: error.stack,
          nodeTag: node?.tagName,
          nodeId: node?.id
        });
        console.error('[FSB] DOM element processing error:', error);
      }
    });

    // CDK overlay scan: Angular Material renders mat-select/autocomplete options in
    // cdk-overlay-container appended to <body>, outside the main component tree.
    // Without this, get_dom_snapshot returns no options after a mat-select opens.
    try {
      const overlayContainer = document.querySelector('.cdk-overlay-container');
      if (overlayContainer) {
        const overlayNodes = overlayContainer.querySelectorAll(
          'mat-option, [role="option"], [role="listbox"], .mat-select-panel *, .mat-autocomplete-panel *'
        );
        overlayNodes.forEach(node => {
          try {
            if (node.nodeType !== Node.ELEMENT_NODE) return;
            const rect = node.getBoundingClientRect();
            if (rect.height === 0 && rect.width === 0) return; // invisible
            const semanticId = generateSemanticElementId(node, totalElementCount);
            try { node.setAttribute('data-fsb-id', semanticId); } catch (_e) {}
            const overlayElementData = {
              elementId: semanticId,
              type: node.tagName.toLowerCase(),
              description: generateElementDescription(node),
              text: node.innerText?.trim() || node.textContent?.trim() || '',
              id: node.id || '',
              class: node.className ? String(node.className) : '',
              position: { x: Math.round(rect.left), y: Math.round(rect.top), width: Math.round(rect.width), height: Math.round(rect.height), inViewport: true },
              selectors: generateSelectors(node, semanticId).map(s => s.selector),
              interactive: true,
              inViewport: true,
              _fromOverlay: true
            };
            ['aria-label', 'role', 'aria-selected', 'data-value'].forEach(attr => {
              if (node.getAttribute(attr)) {
                if (!overlayElementData.attributes) overlayElementData.attributes = {};
                overlayElementData.attributes[attr] = node.getAttribute(attr);
              }
            });
            viewportElements.push(overlayElementData);
            totalElementCount++;
          } catch (_nodeErr) {}
        });
      }
    } catch (_overlayErr) {}

    // Combine viewport and offscreen elements
    let elements;
    let viewportBudget;
    let offscreenBudget;
    let importantOffscreen = [];

    if (viewportComplete) {
      // Viewport-complete mode: include ALL viewport elements, cap offscreen at 30
      viewportBudget = viewportElements.length;
      offscreenBudget = 30;
      importantOffscreen = offscreenElements
        .filter(el => el.isButton || el.isInput || el.isLink ||
                      el.attributes?.['data-testid'] || el.attributes?.['aria-label'] ||
                      el.isCaptcha)
        .slice(0, 30);
      elements = [...viewportElements, ...importantOffscreen];
    } else if (viewportOnlyMode) {
      viewportBudget = maxElements;
      offscreenBudget = 0;
      elements = viewportElements.slice(0, maxElements);
    } else {
      viewportBudget = Math.min(viewportElements.length, Math.floor(maxElements * 0.7));
      offscreenBudget = Math.min(
        offscreenElements.length,
        maxElements - viewportBudget
      );

      importantOffscreen = offscreenElements
        .filter(el => el.isButton || el.isInput || el.isLink ||
                      el.attributes?.['data-testid'] || el.attributes?.['aria-label'] ||
                      el.isCaptcha)
        .slice(0, offscreenBudget);

      elements = [
        ...viewportElements.slice(0, viewportBudget),
        ...importantOffscreen
      ];
    }

    logger.logDOMOperation(FSB.sessionId, 'viewport_first_collection', {
      viewportOnlyMode,
      viewportComplete,
      viewportElements: viewportElements.length,
      offscreenElements: viewportOnlyMode ? 0 : offscreenElements.length,
      viewportBudget,
      offscreenBudget,
      importantOffscreenIncluded: importantOffscreen.length,
      totalCombined: elements.length
    });

    // Apply optimizations
    let processedElements = elements;

    if (prioritizeViewport && !viewportComplete) {
      processedElements = prioritizeElements(processedElements);
      processedElements = processedElements.slice(0, maxElements);
    } else if (prioritizeViewport) {
      processedElements = prioritizeElements(processedElements);
    }

    // Apply DOM diffing
    let elementsToSend = processedElements;
    if (useDiffing) {
      const { changed, unchanged } = diffDOM(processedElements);
      const unchangedLimit = Math.min(
        Math.max(100, Math.floor(unchanged.length * 0.2)),
        200
      );

      const importantUnchanged = unchanged
        .filter(el => el.isButton || el.isInput || el.isLink || isInViewport(el))
        .slice(0, unchangedLimit);
      elementsToSend = [...changed, ...importantUnchanged];
    }

    // Detect page context
    const pageContext = detectPageContext();

    // Build the base DOM structure
    const domStructure = {
      elements: elementsToSend,
      htmlContext: relevantHTML,
      pageContext: pageContext,
      completionSignals: detectCompletionSignals(),
      scrollPosition: {
        x: window.scrollX,
        y: window.scrollY
      },
      viewport: {
        width: window.innerWidth,
        height: window.innerHeight
      },
      scrollInfo: (() => {
        const pageHeight = Math.max(document.body.scrollHeight, document.documentElement.scrollHeight);
        const viewportHeight = window.innerHeight;
        const scrollY = window.scrollY;
        const maxScroll = pageHeight - viewportHeight;
        const atBottom = scrollY >= maxScroll - 10;
        return {
          pageHeight,
          scrollY,
          viewportHeight,
          scrollPercentage: maxScroll > 0 ? Math.round((scrollY / maxScroll) * 100) : 100,
          atTop: scrollY === 0,
          atBottom,
          hasMoreBelow: !atBottom,
          hasMoreAbove: scrollY > 0
        };
      })(),
      url: window.location.href,
      title: document.title,
      captchaPresent: elements.some(el => el.isCaptcha) || pageContext.pageState.hasCaptcha,
      ecommerceContext: extractEcommerceProducts(),
      timestamp: Date.now(),
      optimization: {
        totalElements: elements.length,
        sentElements: elementsToSend.length,
        newElementCount: elementsToSend.filter(el => el.isNew).length,
        usedDiffing: useDiffing,
        usedViewportPriority: prioritizeViewport,
        viewportOnlyMode: viewportOnlyMode
      }
    };

    // Use DOM State Manager for intelligent diffing if available
    if (domStateManager && options.useIncrementalDiff !== false) {
      logger.logDOMOperation(FSB.sessionId, 'dom_capture_start', {
        totalElements: elements.length,
        filteredElements: elementsToSend.length,
        method: 'state_manager'
      });

      const optimizedPayload = domStateManager.generateOptimizedPayload(domStructure);

      domStructure.optimization.diffType = optimizedPayload.type;
      domStructure.optimization.deltaSize = JSON.stringify(optimizedPayload).length;
      domStructure.optimization.fullSize = JSON.stringify(domStructure).length;
      domStructure.optimization.compressionRatio =
        (domStructure.optimization.fullSize - domStructure.optimization.deltaSize) / domStructure.optimization.fullSize;

      logger.logDOMOperation(FSB.sessionId, 'dom_capture_optimized', {
        mode: optimizedPayload.type.toUpperCase(),
        fullSize: domStructure.optimization.fullSize,
        deltaSize: domStructure.optimization.deltaSize,
        compressionRatio: (domStructure.optimization.compressionRatio * 100).toFixed(1) + '%',
        savings: domStructure.optimization.fullSize - domStructure.optimization.deltaSize
      });

      if (optimizedPayload.type === 'delta') {
        const { elements, ...domStructureWithoutElements } = domStructure;

        const viewportElems = (elements || [])
          .filter(el => el.position?.inViewport)
          .slice(0, 30);

        return {
          ...domStructureWithoutElements,
          ...optimizedPayload,
          _isDelta: true,
          _totalElements: elements?.length || 0,
          viewportElements: viewportElems
        };
      }

      return {
        ...domStructure,
        ...optimizedPayload,
        _isDelta: false
      };
    }

    // Apply compact serialization
    const useCompactFormat = options.useCompactFormat !== false;

    if (useCompactFormat && elementsToSend.length > 50) {
      logger.logDOMOperation(FSB.sessionId, 'compact_serialization', { elementCount: elementsToSend.length });
      const serializer = new OptimizedDOMSerializer();
      const compactPayload = serializer.serialize(elementsToSend);

      const originalSize = JSON.stringify(elementsToSend).length;
      const compactSize = JSON.stringify(compactPayload).length;
      const compressionRatio = serializer.getCompressionRatio(elementsToSend, compactPayload);

      logger.logDOMOperation(FSB.sessionId, 'compact_serialization_complete', {
        originalSize,
        compactSize,
        compressionRatio: (compressionRatio * 100).toFixed(1) + '%',
        stringTableSize: compactPayload.strings.length
      });

      return {
        ...domStructure,
        elements: compactPayload.elements,
        stringTable: compactPayload.strings,
        format: 'compact',
        version: compactPayload.version,
        optimization: {
          ...domStructure.optimization,
          compressionRatio,
          originalSize,
          compactSize
        }
      };
    }

    logger.logDOMOperation(FSB.sessionId, 'full_dom_capture', {
      totalElements: elements.length,
      sentElements: elementsToSend.length,
      payloadSize: JSON.stringify(domStructure).length
    });

    return domStructure;
  }

  /**
   * Returns a Runtime.evaluate expression string that extracts canvas pixel data
   * as a color grid + edge detection fallback when draw call interception is unavailable.
   * Used when the interceptor fails (WebGL, CORS taint, page loaded before extension).
   * @returns {string} JavaScript expression for Runtime.evaluate
   */
  function getCanvasPixelFallback() {
    return `(() => {
      const canvases = document.querySelectorAll('canvas');
      if (!canvases.length) return JSON.stringify({ error: 'NO_CANVAS_FOUND', scenes: [] });

      function colorName(r, g, b) {
        if (r > 220 && g > 220 && b > 220) return 'white';
        if (r < 40 && g < 40 && b < 40) return 'black';
        if (r > 150 && g < 80 && b < 80) return 'red';
        if (r < 80 && g > 150 && b < 80) return 'green';
        if (r < 80 && g < 80 && b > 150) return 'blue';
        if (r > 150 && g > 150 && b < 80) return 'yellow';
        if (r > 100 && g > 150 && b > 200) return 'light-blue';
        if (r > 150 && g > 100 && b < 80) return 'orange';
        if (r > 150 && g < 100 && b > 150) return 'purple';
        if (r > 120 && g > 120 && b > 120) return 'gray';
        return 'other';
      }

      const scenes = [];

      for (let ci = 0; ci < Math.min(canvases.length, 3); ci++) {
        const canvas = canvases[ci];
        let ctx;
        try { ctx = canvas.getContext('2d'); } catch(e) { continue; }
        if (!ctx) continue;

        const w = canvas.width, h = canvas.height;
        if (w < 10 || h < 10) continue;

        let data;
        try { data = ctx.getImageData(0, 0, w, h).data; } catch(e) {
          scenes.push({ canvas: ci, error: 'CORS_TAINTED', width: w, height: h });
          continue;
        }

        // --- Color Grid: 8x6 regions ---
        const gridCols = 8, gridRows = 6;
        const cellW = Math.floor(w / gridCols), cellH = Math.floor(h / gridRows);
        const colorRegions = [];

        for (let row = 0; row < gridRows; row++) {
          for (let col = 0; col < gridCols; col++) {
            const x0 = col * cellW, y0 = row * cellH;
            const counts = {};
            const step = Math.max(4, Math.floor(Math.min(cellW, cellH) / 8));
            let total = 0;
            for (let py = y0; py < y0 + cellH; py += step) {
              for (let px = x0; px < x0 + cellW; px += step) {
                const idx = (py * w + px) * 4;
                const name = colorName(data[idx], data[idx+1], data[idx+2]);
                counts[name] = (counts[name] || 0) + 1;
                total++;
              }
            }
            const sorted = Object.entries(counts).sort((a,b) => b[1] - a[1]);
            const dominant = sorted[0][0];
            const pct = Math.round(sorted[0][1] / total * 100);
            if (dominant !== 'white' || pct < 85) {
              const rowNames = ['top','upper','mid-upper','mid-lower','lower','bottom'];
              const colNames = ['far-left','left','center-left','center','center-right','right','far-right','edge'];
              colorRegions.push({
                pos: rowNames[row] + ' ' + colNames[col],
                color: dominant,
                pct: pct,
                secondary: sorted.length > 1 && sorted[1][1]/total > 0.08 ? sorted[1][0] : null
              });
            }
          }
        }

        // --- Edge Detection: Sobel-like ---
        const edgeCols = 80, edgeRows = 30;
        const eCellW = w / edgeCols, eCellH = h / edgeRows;
        const threshold = 0.15;
        let edgeLines = [];
        let edgeCount = 0;

        for (let row = 0; row < edgeRows; row++) {
          let line = '';
          for (let col = 0; col < edgeCols; col++) {
            const cx = Math.floor(col * eCellW + eCellW / 2);
            const cy = Math.floor(row * eCellH + eCellH / 2);

            function bright(px, py) {
              const xx = Math.min(Math.max(px, 0), w - 1);
              const yy = Math.min(Math.max(py, 0), h - 1);
              const i = (yy * w + xx) * 4;
              return (data[i] * 0.299 + data[i+1] * 0.587 + data[i+2] * 0.114) / 255;
            }

            const gx = bright(cx + Math.floor(eCellW), cy) - bright(cx - Math.floor(eCellW), cy);
            const gy = bright(cx, cy + Math.floor(eCellH)) - bright(cx, cy - Math.floor(eCellH));
            const mag = Math.sqrt(gx * gx + gy * gy);

            if (mag < threshold) {
              line += ' ';
            } else {
              edgeCount++;
              const angle = Math.atan2(gy, gx) * 180 / Math.PI;
              if (angle >= -22.5 && angle < 22.5) line += '|';
              else if (angle >= 22.5 && angle < 67.5) line += '/';
              else if (angle >= 67.5 && angle < 112.5) line += '-';
              else if (angle >= 112.5 || angle < -112.5) line += '|';
              else if (angle >= -67.5 && angle < -22.5) line += '\\\\';
              else line += '-';
            }
          }
          if (line.trim().length > 0) {
            edgeLines.push(line.replace(/\\s+$/, ''));
          }
        }

        scenes.push({
          canvas: ci,
          width: w,
          height: h,
          colorRegions: colorRegions,
          edgeLineCount: edgeCount,
          edges: edgeLines.length > 0 ? edgeLines.join('\\n') : null
        });
      }

      return JSON.stringify({ scenes: scenes });
    })()`;
  }

